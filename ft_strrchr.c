char	*ft_strrchr(const char *s, int c)
{
	const char	*i;

	i = 0;
	while (*s != '\0')
	{
		if (*s == (char)c)
			i = s;
		s++;
	}
	if (*s == (char)c)
		return ((char *)s);
	else
		return ((char *)i);
}
